//----------------------------------------------------------------------------------------
var info = ms.log.info.bind(this, '[redis]')
var error = ms.log.error.bind(this, '[redis]')
//----------------------------------------------------------------------------------------

var def_config = {
    info      : info,
    error     : error,
    redis_name: '',
    redis_fn  : null,
    key_prefix: '',
    def_time  : 60 * 60 * 24 * 7,
}

//----------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------
function init(option) {
    var _option = Object.assign({}, def_config, option)
    return {
        _option
    }
}

//----------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------
module.exports = {
    init: (option) => init(option)
}